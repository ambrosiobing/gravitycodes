/**
*   Force close button
*/

var modalButtonOnly1a = new tingle.modal({
    closeMethods: [],
    footer: true,
    stickyFooter: true
});
var btn1a = document.querySelector('.js-tingle-modal-1a');
btn1a.addEventListener('click', function(){
    modalButtonOnly1a.open();
});
modalButtonOnly1a.setContent(document.querySelector('.tingle-footer-button-close1a').innerHTML);

modalButtonOnly1a.addFooterBtn('Close', 'tingle-btn tingle-btn--primary tingle-btn--pull-right', function(){
    modalButtonOnly1a.close();
});

